package Condition;

public class PalindromStr {
    public static void main(String[] args) {

        String name = "abccba";
        int start =0;
        int end = name.length();
        while(start<end){
            if(start==end){
                System.out.println("palindrom");
                continue;
            }
            else {
                break;
            }

        }


    }
}
